function somar(número1, número2) {
  return número1 + número2;
}

function subtrair(número1, número2) {
  return número1 - número2;
}

function multiplicar(fator1, fator2) {
  return fator1 + fator2;
}

function dividir(dividendo, divisor) {
  return dividendo / divisor;
}

export default {
  somar,
  subtrair,
  multiplicar,
  dividir
};